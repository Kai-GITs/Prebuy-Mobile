class Product {
  final String name;
  final int price;
  final int amount;
  final String category;
  final String description;
  final double rating;
  final bool available;
  final bool onSale;

  Product({
    required this.name,
    required this.price,
    required this.amount,
    required this.category,
    required this.description,
    required this.rating,
    required this.available,
    required this.onSale,
  });
}

